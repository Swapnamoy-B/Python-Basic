def percent(marks):  # --> Function with argument and defination
    p = int((sum(marks)/400)*100)
    return p


marks1 = [45, 75, 47, 55]
percent1 = percent(marks1)

marks2 = [58, 64, 47, 83]
percent2 = percent(marks2)

print(percent1, percent2)

